a=7
b=2
print("a=",a)
print("b=",b)

print("a+b=",a+b) # somme
print("a-b",a-b) # différence
print("a*b=",a*b) # produit
print("a/b=",a/b) # quotient dans la division décimale
print("a**b=",a**b) # puissance
print("a//b=",a//b) # quotient dans la division euclidienne (entiers)
print("a%b=",a%b) # reste dans la division euclidienne (entiers)